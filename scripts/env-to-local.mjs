#!/usr/bin/env node
/**
 * Reads NASA_API_KEY from root `.env` and writes `js/config.local.js`
 * (gitignored) so the static site can load it via ES module import.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const envPath = path.join(root, ".env");
const outPath = path.join(root, "js", "config.local.js");

if (!fs.existsSync(envPath)) {
  console.error("Missing .env — copy .env.example to .env and set NASA_API_KEY.");
  process.exit(1);
}

const raw = fs.readFileSync(envPath, "utf8");
let key = "";
for (const line of raw.split(/\r?\n/)) {
  const t = line.trim();
  if (!t || t.startsWith("#")) continue;
  const m = t.match(/^NASA_API_KEY\s*=\s*(.*)$/);
  if (m) {
    key = m[1].trim().replace(/^["']|["']$/g, "");
    break;
  }
}

if (!key) {
  console.error("NASA_API_KEY is empty or missing in .env");
  process.exit(1);
}

const escaped = key.replace(/\\/g, "\\\\").replace(/'/g, "\\'");
const body = `// Generated by scripts/env-to-local.mjs from root .env — do not commit.\nexport const NASA_API_KEY = '${escaped}';\n`;
fs.writeFileSync(outPath, body, "utf8");
console.log("Wrote js/config.local.js from .env");
